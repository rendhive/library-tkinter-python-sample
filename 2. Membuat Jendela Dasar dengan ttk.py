import tkinter as tk
from tkinter import ttk

def create_window():
    window = tk.Tk()
    window.title("Jendela Dasar dengan TTK")
    window.mainloop()

create_window()
# Fungsi: Membuat jendela dasar menggunakan tkinter.ttk.
